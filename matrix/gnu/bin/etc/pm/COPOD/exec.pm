package etc::pm::COPOD::exec;

use strict;
use warnings;
use utf8;

use vars qw($VERSION);

$VERSION = '0.0.1';

sub new {
    my $class = shift;
    my $self = {};
    bless $self, $class if $self;
    return $self->{false};
}

sub exec {
